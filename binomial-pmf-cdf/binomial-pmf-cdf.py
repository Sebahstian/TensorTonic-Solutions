import math
def binomial_pmf_cdf(n, p, k):
    """
    Compute Binomial PMF and CDF.
    """
    pmf = (math.comb(n, k)) * (p ** k) * ((1 - p) ** (n - k))
    
    cdf = 0
    
    for i in range(0, k+1):
      cdf += (math.comb(n, i)) * (p ** i) * ((1 - p) ** (n - i))
    return pmf, cdf